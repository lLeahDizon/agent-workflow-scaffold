export function recommendSkills(profile: ProjectProfile, localSkills: LocalSkill[] = []): SkillRecommendation[] {
  const localById = localSkillById(localSkills);
  return RULES.filter((rule) => rule.applies(profile)).map((rule) => {
    const local = localById.get(rule.id);
    return {
      id: rule.id,
      name: rule.name,
      category: rule.category,
      description: rule.description,
      reason: rule.reason(profile),
      installPolicy: rule.installPolicy,
      installed: Boolean(local),
      localPath: local?.skillPath,
      source: local?.source
    };
  });
}
