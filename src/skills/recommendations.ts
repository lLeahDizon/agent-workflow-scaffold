import type { LocalSkill, ProjectProfile, SkillRecommendation, SkillRecommendationCategory } from "../types.js";

interface RecommendationRule {
  id: string;
  name: string;
  category: SkillRecommendationCategory;
  description: string;
  reason: (profile: ProjectProfile) => string;
  installPolicy: SkillRecommendation["installPolicy"];
  applies: (profile: ProjectProfile) => boolean;
}

function hasAnyDependency(profile: ProjectProfile, names: string[]): boolean {
  const dependencies = new Set([...profile.dependencies, ...profile.devDependencies]);
  return names.some((name) => dependencies.has(name));
}

function hasAnyDoc(profile: ProjectProfile, patterns: RegExp[]): boolean {
  return profile.docFiles.some((file) => patterns.some((pattern) => pattern.test(file)));
}

function localSkillById(skills: LocalSkill[]): Map<string, LocalSkill> {
  return new Map(skills.map((skill) => [skill.id, skill]));
}

